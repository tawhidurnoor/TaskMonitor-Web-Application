    public function index_company(Company $company)
    {
        if (Auth::user()->id != $company->owner_user_id) {
            abort(403, 'Unauthorized action.');
        }

        $staffs = Staff::where('company_id', $company->id)->get();

        return view('backend.staff.index',[
            'staffs' => $staffs,
            'company' => $company,
        ]);
    }


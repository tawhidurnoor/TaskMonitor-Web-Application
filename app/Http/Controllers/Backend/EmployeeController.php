    public function search(Request $request)
    {
        $serach_query = $request->email;
        $users = User::where('email', 'LIKE' ,'%' . $serach_query . '%')->get();
        return view('backend.employee.search_result',[
            'serach_query' => $serach_query,
            'users' => $users,
        ]);
    }

